async function seedCourses() {
  console.log("Seeding Norwegian golf courses...");

  for (const courseData of COURSES) {
    const courseId = nanoid();
    const totalLength = courseData.holes.reduce((sum, h) => sum + h.length, 0);

    await prisma.course.upsert({
      where: { id: courseId },
      update: {},
      create: {
        id: courseId,
        name: courseData.name,
        location: courseData.location,
        country: "NO",
        par: courseData.par,
        totalLength,
        courseRating: courseData.courseRating,
        slopeRating: courseData.slopeRating,
        latitude: courseData.latitude,
        longitude: courseData.longitude,
        updatedAt: new Date(),
        Hole: {
          create: courseData.holes.map((h) => ({
            id: nanoid(),
            holeNumber: h.number,
            par: h.par,
            handicap: h.handicap,
            lengthMeter: h.length,
            teeColor: "yellow",
          })),
        },
      },
    });

    console.log(`  ✓ ${courseData.name} (${courseData.holes.length} hull)`);
  }

  console.log(`\nFerdig! ${COURSES.length} baner seedet.`);
}

seedCourses()
  .catch((e) => {
    console.error("Feil ved seeding:", e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
